package day05operators;

public class Decrement01 {

	public static void main(String[] args) {
		// int data type de bir Veriable olusturun 
		// bu Variable degerini 3 Yontemide kullanarak her dafasin 1 azaltin
		
		int num1=12;
		
		//uzun
		num1=num1-1;
		System.out.println(num1);
		
		//kisa
		
		num1-=1;
		System.out.println(num1);
		
		//en kisa
		num1--;
		System.out.println(num1);
		
		
	}

}
